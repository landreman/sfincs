    ! *******************************************************************************
    !
    ! Build grids and integration weights
    !
    ! *******************************************************************************
    ! *******************************************************************************

    allocate(theta(Ntheta))
    allocate(zeta(Nzeta))

    zetaMax = 2*pi/NPeriods
    theta = [( (2*pi*j)/Ntheta, j=0, Ntheta-1 )]
    zeta = [( (zetaMax*j)/Nzeta, j=0, Nzeta-1 )]

    dtheta = theta(2) - theta(1)
    dzeta = zeta(2) - zeta(1)

    theta_weight = dtheta
    zeta_weight = dzeta * NPeriods

