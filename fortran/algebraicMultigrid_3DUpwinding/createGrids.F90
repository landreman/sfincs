    if (stencil_factor_face   < 0) stop "Error! stencil_factor_face <0"
    if (stencil_factor_edge   < 0) stop "Error! stencil_factor_edge <0"
    if (stencil_factor_vertex < 0) stop "Error! stencil_factor_vertex <0"

