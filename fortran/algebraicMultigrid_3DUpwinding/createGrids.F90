  subroutine set_first_derivative_stencil(first_derivative_option, first_derivative_stencil, overshoot)

    use kinds
    use globalVariables, only: stencil_width, upwinding_factor, masterProc

    implicit none

    integer, intent(in) :: first_derivative_option
    real(prec), dimension(-stencil_width:stencil_width) :: first_derivative_stencil
    real(prec), intent(in) :: overshoot

    first_derivative_stencil = 0
    select case (first_derivative_option)
    case (101)
       first_derivative_stencil( 0) =  1
       first_derivative_stencil(-1) = -1
       if (masterProc) print *,"  1st order (1 point on 1 side, 0 points on the other, upwinded)"
    case (102)
       first_derivative_stencil( 1) =  0.5d+0 - upwinding_factor
       first_derivative_stencil( 0) =  2 * upwinding_factor
       first_derivative_stencil(-1) = -0.5d+0 - upwinding_factor
       if (masterProc) print *,"  1st order (1 point on either side)"
       if (masterProc) print *,"  Upwinding factor:",upwinding_factor
    case (201)
       first_derivative_stencil( 1) =  0.5d+0
       first_derivative_stencil( 0) =  0
       first_derivative_stencil(-1) = -0.5d+0
       if (masterProc) print *,"  2nd order (1 point on either side, centered)"
    case (202)
       first_derivative_stencil( 0) =  3 / (2.0d+0)
       first_derivative_stencil(-1) = -2
       first_derivative_stencil(-2) = 0.5d+0
       if (masterProc) print *,"  2nd order (2 points on 1 side, 0 points on the other, upwinded)"
    case (203)
       first_derivative_stencil( 1) =  0.25d+0
       first_derivative_stencil( 0) =  0.75d+0
       first_derivative_stencil(-1) = -1.25d+0
       first_derivative_stencil(-2) =  0.25d+0
       if (masterProc) print *,"  2nd order (Fromm: 2 points on 1 side, 1 point on the other, upwinded)"
    case (301)
       first_derivative_stencil( 1) =  1 / (3.0d+0)
       first_derivative_stencil( 0) =  0.5d+0
       first_derivative_stencil(-1) = -1
       first_derivative_stencil(-2) =  1 / (6.0d+0)
       if (masterProc) print *,"  3rd order (2 points on 1 side, 1 point on the other, upwinded)"
    case (302)
       first_derivative_stencil( 2) = -1 / (12.0d+0) + upwinding_factor
       first_derivative_stencil( 1) =  2 / (3.0d+0) - 4 * upwinding_factor
       first_derivative_stencil( 0) =  6 * upwinding_factor
       first_derivative_stencil(-1) = -2 / (3.0d+0) - 4 * upwinding_factor
       first_derivative_stencil(-2) =  1 / (12.0d+0) + upwinding_factor
       if (masterProc) print *,"  3rd order (2 points on either side)"
       if (masterProc) print *,"  Upwinding factor:",upwinding_factor
    case (401)
       first_derivative_stencil( 2) = -1 / (12.0d+0)
       first_derivative_stencil( 1) =  2 / (3.0d+0)
       first_derivative_stencil( 0) =  0
       first_derivative_stencil(-1) = -2 / (3.0d+0)
       first_derivative_stencil(-2) =  1 / (12.0d+0)
       if (masterProc) print *,"  4th order (2 points on either side, centered)"
    case (402)
       first_derivative_stencil( 1) =  1 / (4.0d+0)
       first_derivative_stencil( 0) =  5 / (6.0d+0)
       first_derivative_stencil(-1) = -3 / (2.0d+0)
       first_derivative_stencil(-2) =  1 / (2.0d+0)
       first_derivative_stencil(-3) = -1 / (12.0d+0)
       if (masterProc) print *,"  4th order (3 points on 1 side, 1 point on the other, upwinded)"
    case (501)
       first_derivative_stencil( 2) = -1 / (20.0d+0)
       first_derivative_stencil( 1) =  1 / (2.0d+0)
       first_derivative_stencil( 0) =  1 / (3.0d+0)
       first_derivative_stencil(-1) = -1
       first_derivative_stencil(-2) =  1 / (4.0d+0)
       first_derivative_stencil(-3) = -1 / (30.0d+0)
       if (masterProc) print *,"  5th order (3 points on 1 side, 2 points on the other, upwinded)"
    case (502)
       first_derivative_stencil( 3) =  1 / (60.0d+0) - upwinding_factor
       first_derivative_stencil( 2) = -3 / (20.0d+0) + 6 * upwinding_factor
       first_derivative_stencil( 1) =  3 / (4.0d+0) - 15 * upwinding_factor
       first_derivative_stencil( 0) =  20 * upwinding_factor
       first_derivative_stencil(-1) = -3 / (4.0d+0) - 15 * upwinding_factor
       first_derivative_stencil(-2) =  3 / (20.0d+0) + 6 * upwinding_factor
       first_derivative_stencil(-3) = -1 / (60.0d+0) - upwinding_factor
       if (masterProc) print *,"  5th order (3 points on either side)"
       if (masterProc) print *,"  Upwinding factor:",upwinding_factor
    case (601)
       first_derivative_stencil( 3) =  1 / (60.0d+0)
       first_derivative_stencil( 2) = -3 / (20.0d+0)
       first_derivative_stencil( 1) =  3 / (4.0d+0)
       first_derivative_stencil( 0) =  0
       first_derivative_stencil(-1) = -3 / (4.0d+0)
       first_derivative_stencil(-2) =  3 / (20.0d+0)
       first_derivative_stencil(-3) = -1 / (60.0d+0)
       if (masterProc) print *,"  6th order (3 points on either side, centered)"
    case default
       stop "Invalid first_derivative_option."
    end select

    if (masterProc) print *,"  Overshoot = ",overshoot
    first_derivative_stencil = first_derivative_stencil * (1 + overshoot) + overshoot * first_derivative_stencil(stencil_width:-stencil_width:-1)

  end subroutine set_first_derivative_stencil

