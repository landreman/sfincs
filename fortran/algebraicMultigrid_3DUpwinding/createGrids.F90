    if (masterProc) print *,"sum(xi_weights):",sum(xi_weights)
    if (abs(sum(xi_weights)-2) > 1.0e-12) then
       if (masterProc) then
          print *,"Error! xi_weights do not sum to 2!"
          print *,"xi_weights:",xi_weights
          print *,"Sum is",sum(xi_weights)
       end if
       stop
    end if
    if (masterProc) print *,"sum(xi_weights*xi):",sum(xi_weights*xi)
    if (abs(sum(xi_weights*xi)-0) > 1.0e-12) then
       if (masterProc) then
          print *,"Error! xi_weights*xi does not sum to 0!"
          print *,"xi:",xi
          print *,"xi_weights:",xi_weights
          print *,"Sum is",sum(xi_weights*xi)
       end if
       stop
    end if
    if (masterProc) print *,"sum(xi_weights*xi*xi):",sum(xi_weights*xi*xi)
    if (abs(sum(xi_weights*xi*xi)-two/3) > 1.0e-12) then
       if (masterProc) then
          print *,"Error! xi_weights*xi*xi do not sum to 2/3!"
          print *,"xi_weights:",xi_weights
          print *,"Sum is",sum(xi_weights)
       end if
       stop
    end if

